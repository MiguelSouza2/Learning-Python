# IMPORT SERVE PARA CARREGAR MÓDULOS/BIBLIOTECAS NO NOSSO PROGRAMA

# importando a biblioteca padrão 'math' e dando um apelido (alias) que é opcional
import math as mm

# imprimindo o módulo de mostrar o pi
print(mm.pi)

# utilizando a função 'sqrt' da biblioteca 'math' para calcular a raiz quadrada de um número
print(mm.sqrt(25))

# utilizando a função 'pow' da biblioteca 'math' para calcular a potência de um número
print(mm.pow(2,3))

# utilizando a função 'factorial' da biblioteca 'math' para calcular o fatorial de um número
print(mm.factorial(3))

# utilizando a função 'log' da biblioteca 'math' para calcular o logaritmo de um número
print(mm.log(100, 10))

